#include <stdio.h>
#include <stdlib.h>

int main()
{
    int cont;
    cont = 0;         /*Inicialização da variavel contadora*/

    while(cont <= 10){   /*Teste do laço de repetição*/

        printf("%d \n", cont);


        cont ++;          /*Incremento da variavel contadora*/
    }


    return 0;
}
